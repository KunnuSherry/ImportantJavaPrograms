public class Main {
    public static void main(String[] args) {
        int[] marks = {100, 90, 80, 70, 60}; // these are the marks of 5 students
        int sum = 0;
        for(int element:marks){
            sum = sum + element ;
        }
        System.out.println("The average marks is : " + sum/5);







    }
}
